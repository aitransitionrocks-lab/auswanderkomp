// Generiert SQL-Inserts für task_templates aus src/data/tasks.json.
// Run: node scripts/generate-templates-sql.mjs > supabase/migrations/0007_seed_templates.sql

import { readFileSync } from "node:fs";

const tasks = JSON.parse(
  readFileSync(new URL("../src/data/tasks.json", import.meta.url), "utf8")
);

// tasks.json phase → DB phase
const PHASE_MAP = {
  prep: "vorbereitung",
  exit: "umzug",
  arrival: "ankunft",
};

// Inferred category from title keywords
function categorize(title) {
  const t = title.toLowerCase();
  if (/steuer|wegzug|astg|gmbh|tin|nif|sozial|finanz|kosten|konto|bank|cuil|cdi|ruc/.test(t))
    return "finanzen";
  if (/krankenversicherung|kv\b|gkv|pkv|gesundheit|gesy|sns|caja|sgss/.test(t))
    return "versicherung";
  if (/visum|residenc|emirates id|cedula|aufenthalt|dni|abmeldung|residência|empadronamiento|kimlik|tie|yellow slip/.test(t))
    return "recht";
  if (/schule|kind|kita|sorgerecht|familie/.test(t)) return "familie";
  if (/miete|wohn|haus|immobil|ejari/.test(t)) return "wohnen";
  if (/arbeit|job|gewerbe|freelancer|firma|unternehm/.test(t)) return "arbeit";
  if (/sprach|integration/.test(t)) return "sprache";
  return "sonstiges";
}

function esc(s) {
  if (s === null || s === undefined) return "NULL";
  return "'" + String(s).replace(/'/g, "''") + "'";
}

const rows = [];
let order = 0;

for (const [country, list] of Object.entries(tasks)) {
  for (const t of list) {
    const phase = PHASE_MAP[t.phase] ?? "vorbereitung";
    const category = categorize(t.title);
    rows.push(
      `(${esc(country)}, ${esc(category)}, ${esc(t.title)}, ${esc(t.desc)}, ${esc(t.prio)}, ${esc(phase)}, ${t.deadlineDays || 0}, ${order++})`
    );
  }
}

// Generische Tasks für 'unklar' = Top-Titel die in >=5 Ländern vorkommen
const titleCount = new Map();
for (const list of Object.values(tasks)) {
  for (const t of list) {
    titleCount.set(t.title, (titleCount.get(t.title) ?? 0) + 1);
  }
}
const generic = new Set(
  [...titleCount.entries()].filter(([, n]) => n >= 5).map(([title]) => title)
);

const seen = new Set();
for (const list of Object.values(tasks)) {
  for (const t of list) {
    if (!generic.has(t.title) || seen.has(t.title)) continue;
    seen.add(t.title);
    const phase = PHASE_MAP[t.phase] ?? "vorbereitung";
    const category = categorize(t.title);
    rows.push(
      `('unklar', ${esc(category)}, ${esc(t.title)}, ${esc(t.desc)}, ${esc(t.prio)}, ${esc(phase)}, ${t.deadlineDays || 0}, ${order++})`
    );
  }
}

const sql = `-- Migration 0007: Task-Templates Seed (aus src/data/tasks.json generiert)
-- Generated by scripts/generate-templates-sql.mjs

insert into public.task_templates
  (country, category, title, description, priority, phase, lead_time_days, sort_order)
values
${rows.join(",\n")};
`;

process.stdout.write(sql);
